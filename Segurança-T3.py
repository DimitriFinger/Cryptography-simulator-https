def AES_decrypt(message, password):
    bytes_message = bytes.fromhex(message)
    bytes_password = bytes.fromhex(password)
    iv = bytes_message[:16]
    cipher = AES.new(bytes_password, AES.MODE_CBC, iv)
    return (cipher.decrypt(bytes_message[16:]))



