def get_password(V):
    V_hexadecimal = hex(V)[2:]
    h = SHA256.new()
    h.update(bytes.fromhex(V_hexadecimal))
    S = h.hexdigest().upper()  
    print('\nS value:\n' + S)    
    return S[:32]

